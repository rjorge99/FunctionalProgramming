// Pure Function: Creates and returns a new value based on the input parameters
// No side effects
// Must have input parameters
// Doesnt depend on outside variables
//      Reusable
//      Composable
//      Easy to test

// Pure Function
function add(a, b) {
    return a + b;
}

// Impure Function (Procedure)
let counter = 0;
function increment() {
    counter++;
}
